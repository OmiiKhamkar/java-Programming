public class Array2 {
    public static void main(String[] args) {
        int a[]= {11,22,33,44};
        char b[]= {'H','e','l','l','o'};
        String c[]={"Mahendra","Virat","Rohit","Hardik","Jasprit"};
        System.out.print("Contnet of array a is ");
        for (int e : a) {
            System.out.print(e+" ");
        }
        System.out.print("\nContnet of array b is ");
        for (char f : b) {
            System.out.print(f+" ");
        }
        System.out.print("\nContnet of array c is ");
        for (String g : c) {
            System.out.print(g+" ");
        }
    }
}
