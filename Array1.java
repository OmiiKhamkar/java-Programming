public class Array1 {
    public static void main(String[] args) {
        int a[]= {10,20,30,40,50,60};
        char b[]= {'I','N','D','I','A'};
        String c[]={"Pune","Mumbai","Nashik","Nagpur","Nanded"};
        System.out.print("Contnet of array a is ");
        for (int i = 0; i < a.length; i++) {
            System.out.print(a[i]+" ");
        }
        System.out.print("\nContnet of array b is ");
        for (int i = 0; i < b.length; i++) {
            System.out.print(b[i]+" ");
        }
        System.out.print("\nContnet of array c is ");
        for (int i = 0; i < c.length; i++) {
            System.out.print(c[i]+" ");
        }
    }
}
